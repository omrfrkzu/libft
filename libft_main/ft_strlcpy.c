#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;

	if (dstsize == 0)
		return (ft_strlen(src));
	i = 0;
	while (src[i] && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = 0;
	return (ft_strlen(src));
}

#include <stdio.h>
int main()
{
	char dst[] = "erik";
	const char src[] = "armut";
	
	size_t res = ft_strlcpy(dst, src, sizeof(dst));
	printf("%s\n", dst);
	printf("%zu", res);
}
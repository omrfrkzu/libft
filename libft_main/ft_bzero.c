#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	ft_memset(s, 0, n);
}

#include <stdio.h>

int main()
{
	char s[] = "omer";
	ft_bzero(s, 4);
	printf("%d, %d", s[0], s[1]);
}
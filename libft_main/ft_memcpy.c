#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	i;

	if (n == 0 || dst == src)
		return (dst);
	i = 0;
	while (i < n)
	{
		((unsigned char *) dst)[i] = ((unsigned char *) src)[i];
		i++;
	}
	return (dst);
}

#include <stdio.h>
int main()
{
	char s1[15];
	const char *s2 = "omer faruk";
	char *result = ft_memcpy(s1, s2, 10 + 1);
	printf("%s", result);
}